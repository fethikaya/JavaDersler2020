package day02variables;

public class Variables02 {

	public static void main(String[] args) {
		
		int fiyat = 500;
		System.out.println (fiyat);
		System.out.println ("fiyat");
		
		int ekmek = 3;
		int peynir = 21;
		System.out.println (ekmek + peynir);
		
		boolean yasli = true;
		System.out.println(yasli);
		
		char ilkHarf = 'S'; /// satýr sonlarýna noktalý virgül koyuyoruz.
		System.out.println (ilkHarf);

		byte yas = 83;
		System.out.println(yas);
		
		short nufusKoy = 23000;
		System.out.println(nufusKoy);
		
		int hazinePara = 32356484;
		System.out.println(hazinePara);

		long hazinePara2 = 879666598;
		System.out.println(hazinePara2);
		
		float borc = 12.53f; /// virgül deðil nokta.
		System.out.println(borc);
		
		double borc2 = 12.53;
		System.out.println(borc2);
		
				
	}

}
