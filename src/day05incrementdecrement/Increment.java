package day05incrementdecrement;

public class Increment {

	public static void main(String[] args) {
		
		int s = 12;
		s = s + 3;
		System.out.println(s);

		int c = 16;
		c = c + 8;
		System.out.println(c);

		c += 8;
		System.out.println(c);
		
		c ++;
		System.out.println(c);
		
		
		int d = 42;
		d = d + 18;
		System.out.println(d);
		
		d +=18;
		System.out.println(d);
		
		d ++;
		System.out.println(d);
		
		
		
		
		int e = 25;
		e = e*5;
		System.out.println(e);
		
		e *=5;
		System.out.println(e);
		
		
	}

}
